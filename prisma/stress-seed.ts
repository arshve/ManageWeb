const localPath = path.join(process.cwd(), '.env.local');
if (!existsSync(localPath)) {
  console.error('Refusing to run: .env.local not found.');
  console.error('  Stress seed only runs against a local DB you control.');
  console.error('  Copy .env.local.example to .env.local and fill in your local Postgres credentials.');
  process.exit(1);
}
config({ path: localPath, override: true });

function assertSafeTarget() {
  const url = process.env.DATABASE_URL ?? '';
  const masked = url.replace(/:\/\/[^@]+@/, '://***@');
  const isLocal =
    /@(localhost|127\.0\.0\.1|::1)[:/]/.test(url) ||
    /@host\.docker\.internal[:/]/.test(url);
  const forced = process.env.STRESS_ALLOW_REMOTE === '1';
  if (!url) {
    console.error('Refusing to run: DATABASE_URL is empty in .env.local');
    process.exit(1);
  }
  if (!isLocal && !forced) {
    console.error('Refusing to run: DATABASE_URL in .env.local is not a local host.');
    console.error(`  url: ${masked}`);
    console.error('  expected hostname: localhost / 127.0.0.1');
    console.error('  override: STRESS_ALLOW_REMOTE=1 npm run seed:stress');
    process.exit(1);
  }
  console.log(`Target: ${masked}`);
}

